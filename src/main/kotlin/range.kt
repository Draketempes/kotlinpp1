fun main(args: Array<String>) {
    print("Number Range: ")

    for (num in 3..9){
        println(num)
    }

    for (num2 in 10..15){
        println(num2)
    }
    print("Character Range: ")
    for (char in 'c'..'i'){
        println(char)
    }
    print ("Character range: ")
    for (char in 'l'..'t'){
        println(char)
    }
}